package empty;

import java.util.Date;

public class Journal {
	private int journal_id;
	private String title;
	private String author;
	private String summary;
	private String content;
	private int classify;
	private String imgpath;
	private Date datetime;
	private int private_is;
	private int private_pass;

	public int getJournal_id() {
		return journal_id;
	}

	public void setJournal_id(int journal_id) {
		this.journal_id = journal_id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getAuthor() {
		return author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}

	public String getSummary() {
		return summary;
	}

	public void setSummary(String summary) {
		this.summary = summary;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public int getClassify() {
		return classify;
	}

	public void setClassify(int classify) {
		this.classify = classify;
	}

	public String getImgpath() {
		return imgpath;
	}

	public void setImgpath(String imgpath) {
		this.imgpath = imgpath;
	}

	public Date getDatetime() {
		return datetime;
	}

	public void setDatetime(Date datetime) {
		this.datetime = datetime;
	}

	public int getPrivate_is() {
		return private_is;
	}

	public void setPrivate_is(int private_is) {
		this.private_is = private_is;
	}

	public int getPrivate_pass() {
		return private_pass;
	}

	public void setPrivate_pass(int private_pass) {
		this.private_pass = private_pass;
	}

	public Journal() {
		journal_id = 0;
	}

	public Journal(String title, String author, String summary, String content, int classify, String imgpath,
			Date datetime, int private_is, int private_pass) {
		this.title = title;
		this.author = author;
		this.summary = summary;
		this.content = content;
		this.classify = classify;
		this.imgpath = imgpath;
		this.datetime = datetime;
		this.private_is = private_is;
		this.private_pass = private_pass;
	}

	@Override
	public String toString() {
		return "Journal [journal_id=" + journal_id + ", title=" + title + ", author=" + author + ", summary=" + summary
				+ ", content=" + content + ", classify=" + classify + ", imgpath=" + imgpath + ", datetime=" + datetime
				+ ", private_is=" + private_is + ", private_pass=" + private_pass + "]";
	}

}
